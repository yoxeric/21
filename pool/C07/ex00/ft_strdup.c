#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*new;
	int		i;
	int		s;

	s = 0;
	while (src[s])
		s++;
	new = (char *)malloc(sizeof(char) * (s + 1));
	if (!(new))
		return (NULL);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
