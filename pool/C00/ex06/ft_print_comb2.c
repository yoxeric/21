#include<unistd.h>

void	prt2(int i)
{
	char	c;

	c = i / 10 + '0';
	write(1, &c, 1);
	c = i % 10 + '0';
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	x;
	int	y;

	x = 0;
	y = 1;
	while (x <= 98)
	{
		while (y <= 99)
		{
			prt2(x);
			write(1, " ", 1);
			prt2(y);
			if (x != 98)
			{
				write(1, ",", 1);
				write(1, " ", 1);
			}
			y++;
		}
		x++;
		y = x + 1;
	}
}
